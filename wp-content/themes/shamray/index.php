          <h3 class="index-faq__question">Как выбирать экраны для батарей?</h3>
          <div class="index-faq__answer">
            <p>Современные батареи отличаются высокой эффективностью и долговечностью. Однако, не всегда их внешний вид соответствует нашим вкусам и общему стилю помещения, где они будут установлены. Поэтому обратите внимание на декоративные экраны, которые украсят ваши радиаторы.<br>
              Экраны выполняют несколько задач: это декоративное предназначение; защита от ожогов маленьких детей; кроме того, экраны легче промыть, чем саму батарею. В некоторых случаях можно даже увеличить теплоотдачу радиаторов. В продаже можно встретить большое разнообразие разных решёток для батарей. Все они выполнены из различных материалов. Но все экраны объединяет одно — передняя панель выполнена в виде решётки, для лучшей циркуляции воздуха.<br>
              Самый дешёвый вариант — пластик, обратите внимание на то, чтобы материал не выделял при нагревании вредных веществ. Решётки из металла хороший вариант, особенно из стали, не теряют привлекательности, обеспечивают хороший теплообмен.</p>
          </div>
          <h3 class="index-faq__question">Чем полезны экраны для батарей?</h3>
          <div class="index-faq__answer">
            <p>Улучшением теплоотдачи. Климат России славится своими морозными зимами. Имеющие собственную систему отопления, могут не беспокоиться. Им достаточно просто поднять температуру обогрева. В домах с центральным отоплением улучшить теплоотдачу помогут кожухи или короба с теплоотражающей поверхностью. Схема работы проста. На стену за радиатором и на внутреннюю часть кожуха крепится теплоотражающая поверхность или термальный уплотнитель, который не поглощает тепло (откроем маленький секрет: такой уплотнитель можно купить в сети строительных магазинов самостоятельно, и опять-таки прибегнув к собственным навыкам, утеплить понравившуюся модель). В итоге выделяемое тепло не уходит в стену, а через специальные отверстия направляется в помещение. Специалисты советуют: устанавливая любую конструкцию, стену обязательно надо утеплить уплотнителем. Это исключит расход тепла на прогрев стен.</p>
